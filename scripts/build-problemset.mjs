import { mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = fileURLToPath(new URL("..", import.meta.url));
const OUT_FILE = join(ROOT, "public", "data.js");
const CF_URL = "https://codeforces.com/api/problemset.problems";

function problemKey(problem) {
  if (!problem) return null;
  const contestId = problem.contestId ?? problem.contest_id;
  const index = problem.index;
  if (contestId === undefined || index === undefined) return null;
  return `${contestId}/${index}`;
}

function compactProblemset(problemset) {
  const solvedByKey = new Map();
  for (const stat of problemset.problemStatistics || []) {
    const key = problemKey(stat);
    if (key) solvedByKey.set(key, stat.solvedCount || 0);
  }

  return {
    generatedAt: new Date().toISOString(),
    source: "Codeforces problemset.problems",
    problems: (problemset.problems || [])
      .map((problem) => {
        const key = problemKey(problem);
        if (!key) return null;
        return {
          contestId: problem.contestId,
          index: problem.index,
          name: problem.name,
          type: problem.type,
          rating: problem.rating || null,
          tags: problem.tags || [],
          solvedCount: solvedByKey.get(key) || 0
        };
      })
      .filter(Boolean),
    problemStatistics: []
  };
}

const response = await fetch(CF_URL);
const payload = await response.json();

if (payload.status !== "OK") {
  throw new Error(`Codeforces problemset fetch failed: ${payload.comment || response.status}`);
}

const problemset = compactProblemset(payload.result);
const body = [
  "// Generated by scripts/build-problemset.mjs.",
  "// Do not edit this file manually; run `npm run build:problemset` to refresh it.",
  `window.CF_LEARNING_PROBLEMSET = ${JSON.stringify(problemset)};`,
  ""
].join("\n");

await mkdir(dirname(OUT_FILE), { recursive: true });
await writeFile(OUT_FILE, body, "utf8");

console.log(`Wrote ${problemset.problems.length} problems to ${OUT_FILE}`);
